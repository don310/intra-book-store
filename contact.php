include('db_conn.php');

// Initialize variables for storing messages
$messages = [];

// Check if the form is submitted
if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $name = $_POST['name'];
    $email = $_POST['email'];
    $message = $_POST['message'];
    
    // Prepare and bind
    $stmt = $conn->prepare("INSERT INTO contact (name, email, message, submitted_at) VALUES (?, ?, ?, NOW())");

    // Bind parameters using bindValue
    $stmt->bindValue(1, $name, PDO::PARAM_STR);
    $stmt->bindValue(2, $email, PDO::PARAM_STR);
    $stmt->bindValue(3, $message, PDO::PARAM_STR);
    
    // Execute the statement
    if ($stmt->execute()) {
        $messages[] = "Message sent successfully!";
    } else {
        $messages[] = "Error: " . $stmt->errorInfo()[2]; // Use errorInfo() for PDO
    }
    
    // Close the statement
    $stmt->closeCursor(); // Close cursor instead of closing the statement
}
